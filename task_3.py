# Using python as a calculator.
# Make a program with 2 numbers saved in separate variables a and b, then print the result for each of the following:
# Addition, subtraction, division, multiplication, exponent(Power), modulus, floor division

a = 12
b = 8

# Addition
print(f'{a} + {b} =', a + b)
# subtraction
print(f'{a} - {b} =', a - b)
# division
print(f'{a} / {b} =', a / b)
# multiplication
print(f'{a} * {b} =', a * b)
# exponent(Power)
print(f'{a} ** {b} =', a ** b)
# modulus
print(f'{a} % {b} =', a % b)
# floor division
print(f'{a} / {b} =', int(a / b))
